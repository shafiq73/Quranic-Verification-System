# Base64 string ko wapas real audio bytes mein convert karna
audio_bytes = base64.b64decode(bismillah_audio_base64.strip())

correct_text = "بِسْمِ اللَّهِ الرَّحْمَٰنِ الرَّحِيمِ"

# Reference Text
st.markdown("### 🟢 Reference Text (Sahi Tarika):")
